package org.astivetoolkit.agi;


/**
 * Thrown by AGI protocol(e.g. I/O operation issues or
 * malformed commands).
 *
 * @since 1.0.0
 */
public class AgiException extends Exception {
  /**
   * Create a new AgiException object.
   */
  public AgiException() {
  }

  /**
   * Create a new AgiException object with message.
   *
   * @param msg information generated by the exception.
   */
  public AgiException(String msg) {
    super(msg);
  }

  /**
   * Creates a new AgiException object.
   *
   * @param e DOCUMENT ME!
   */
  public AgiException(Exception e) {
    super(e);
  }
}
